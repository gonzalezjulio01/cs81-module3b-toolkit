function double(n) {
  return n * 2;
}

// square(n) - returns the number multiplied by itself
function square(n) {
  return n * n;
}

// isEven(n) - returns true if n is even, false if not
function isEven(n) {
  return n % 2 === 0;
}

// isOdd(n) - returns true if n is odd, false if not
function isOdd(n) {
  return n % 2 !== 0;
}
// multiply(a, b) - returns the product of a and b
function multiply(a, b) {
  return a * b;
}
